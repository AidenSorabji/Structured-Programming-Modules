## Definitions ##

# Integer represents a whole number that could be positive or negetive, but not a decimal (ex. 5, 4, 7, 2)

# Float represents a number with a decimal (ex. 5.675, 2.5849, 8.573935)

# String represents a sequence of unicode numbers (ex. This is an example of a unicode character (or characters) )


## Difference ##

# The difference between float and integer division is pretty small. Integer division is meant to work with whole numbers only, while 
# float works with both whole numbers and decimal numbers


## Storing Data Types ##

# It is very important to store each different data types as if they were stored the same way, they could be used confused with each other and might work weirdly
